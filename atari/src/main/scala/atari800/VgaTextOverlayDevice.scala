    defChar(0x3F, Seq(0x00,0x00,0x3C,0x66,0x06,0x0C,0x18,0x18,0x00,0x18,0x18,0x00,0x00,0x00,0x00,0x00))
    // '@' = 0x40
    defChar(0x40, Seq(0x00,0x00,0x3C,0x66,0xDE,0xD6,0xDE,0xC0,0xC0,0x62,0x3C,0x00,0x00,0x00,0x00,0x00))
    // '\\' = 0x5C
    defChar(0x5C, Seq(0x00,0x00,0x80,0xC0,0x60,0x30,0x18,0x0C,0x06,0x02,0x00,0x00,0x00,0x00,0x00,0x00))
    // ';' = 0x3B
    defChar(0x3B, Seq(0x00,0x00,0x00,0x00,0x18,0x18,0x00,0x00,0x00,0x18,0x18,0x08,0x10,0x00,0x00,0x00))
    // '&' = 0x26
    defChar(0x26, Seq(0x00,0x00,0x38,0x6C,0x6C,0x38,0x76,0xCC,0xCC,0xCC,0x76,0x00,0x00,0x00,0x00,0x00))
    // '$' = 0x24
    defChar(0x24, Seq(0x00,0x10,0x7C,0xD6,0xD0,0x70,0x1C,0x16,0xD6,0x7C,0x10,0x00,0x00,0x00,0x00,0x00))
    // '{' = 0x7B
    defChar(0x7B, Seq(0x00,0x00,0x0E,0x18,0x18,0x18,0x70,0x18,0x18,0x18,0x0E,0x00,0x00,0x00,0x00,0x00))
    // '}' = 0x7D
    defChar(0x7D, Seq(0x00,0x00,0x70,0x18,0x18,0x18,0x0E,0x18,0x18,0x18,0x70,0x00,0x00,0x00,0x00,0x00))
    // '|' = 0x7C
    defChar(0x7C, Seq(0x00,0x00,0x18,0x18,0x18,0x18,0x00,0x18,0x18,0x18,0x18,0x00,0x00,0x00,0x00,0x00))

    data.map(b => BigInt(b.toInt & 0xFF)).toSeq
  }

  // ========================================================================
  // Memories (all in system clock domain)
  // ========================================================================

  val charRam = Mem(Bits(8 bits), CHAR_COUNT)
  val attrRam = Mem(Bits(8 bits), CHAR_COUNT)
  val fontRom = Mem(Bits(8 bits), 4096) init(fontData.map(b => B(b, 8 bits)))

  val palette = Vec(Reg(Bits(16 bits)), 16)
  for (i <- 0 until 16) {
    palette(i).init(B(defaultPalette(i), 16 bits))
  }

  // ========================================================================
  // Control Registers
  // ========================================================================

  val enabled     = Reg(Bool()) init(False)
  val cursorCol   = Reg(UInt(7 bits)) init(0)
  val cursorRow   = Reg(UInt(5 bits)) init(0)
  val defaultAttr = Reg(Bits(8 bits)) init(0x0F)  // White on black

  // ========================================================================
  // Scroll / Clear State Machine
  // ========================================================================

  val scrollBusy = Reg(Bool()) init(False)
  val clearBusy  = Reg(Bool()) init(False)

  val scrollSrcAddr  = Reg(UInt(log2Up(CHAR_COUNT) bits)) init(0)
  val scrollDstAddr  = Reg(UInt(log2Up(CHAR_COUNT) bits)) init(0)
  val scrollCount    = Reg(UInt(log2Up(CHAR_COUNT + 1) bits)) init(0)
  val scrollCharData = Reg(Bits(8 bits)) init(0)
  val scrollAttrData = Reg(Bits(8 bits)) init(0)
  val scrollPhase    = Reg(UInt(2 bits)) init(0)

  val clearAddr    = Reg(UInt(log2Up(CHAR_COUNT) bits)) init(0)
  val clearEndAddr = Reg(UInt(log2Up(CHAR_COUNT) bits)) init(CHAR_COUNT - 1)

  // Memory write port arbitration
  val sysCharWrEn   = False
  val sysCharWrAddr = UInt(log2Up(CHAR_COUNT) bits)
  val sysCharWrData = Bits(8 bits)
  val sysAttrWrEn   = False
  val sysAttrWrAddr = UInt(log2Up(CHAR_COUNT) bits)
  val sysAttrWrData = Bits(8 bits)

  sysCharWrAddr := 0
  sysCharWrData := 0
  sysAttrWrAddr := 0
  sysAttrWrData := 0

  // Memory read port for scroll
  val sysCharRdAddr = UInt(log2Up(CHAR_COUNT) bits)
  sysCharRdAddr := 0
  val sysCharRdData = charRam.readSync(sysCharRdAddr)
  val sysAttrRdData = attrRam.readSync(sysCharRdAddr)

  // Write ports (pipelined to break critical path)
  charRam.write(
    address = RegNext(sysCharWrAddr),
    data    = RegNext(sysCharWrData),
    enable  = RegNext(sysCharWrEn) init(False)
  )
  attrRam.write(
    address = RegNext(sysAttrWrAddr),
    data    = RegNext(sysAttrWrData),
    enable  = RegNext(sysAttrWrEn) init(False)
  )

  // Scroll state machine
  when(scrollBusy) {
    switch(scrollPhase) {
      is(0) {
        sysCharRdAddr := scrollSrcAddr
        scrollPhase := 1
      }
      is(1) {
        sysCharRdAddr := scrollSrcAddr
        scrollCharData := sysCharRdData
        scrollAttrData := sysAttrRdData
        scrollPhase := 2
      }
      is(2) {
        sysCharWrEn   := True
        sysCharWrAddr := scrollDstAddr
        sysCharWrData := scrollCharData
        sysAttrWrEn   := True
        sysAttrWrAddr := scrollDstAddr
        sysAttrWrData := scrollAttrData

        when(scrollCount === 0) {
          scrollBusy := False
          scrollPhase := 0
        } otherwise {
          scrollSrcAddr := scrollSrcAddr + 1
          scrollDstAddr := scrollDstAddr + 1
          scrollCount   := scrollCount - 1
          scrollPhase := 0
        }
      }
    }
  }

  // Clear state machine
  when(clearBusy) {
    sysCharWrEn   := True
    sysCharWrAddr := clearAddr
    sysCharWrData := 0x20
    sysAttrWrEn   := True
    sysAttrWrAddr := clearAddr
    sysAttrWrData := defaultAttr

    when(clearAddr === clearEndAddr) {
      clearBusy := False
    } otherwise {
      clearAddr := clearAddr + 1
    }
  }

  // ========================================================================
  // Cursor auto-advance with scroll
  // ========================================================================

  def advanceCursor(): Unit = {
    when(cursorCol >= (COLS - 1)) {
      cursorCol := 0
      when(cursorRow >= (ROWS - 1)) {
        scrollBusy    := True
        scrollSrcAddr := COLS
        scrollDstAddr := 0
        scrollCount   := ((ROWS - 1) * COLS - 1)
        scrollPhase   := 0
      } otherwise {
        cursorRow := cursorRow + 1
      }
    } otherwise {
      cursorCol := cursorCol + 1
    }
  }

  val scrollWasBusy = RegNext(scrollBusy) init(False)
  when(scrollWasBusy && !scrollBusy) {
    clearBusy    := True
    clearAddr    := ((ROWS - 1) * COLS)
    clearEndAddr := (CHAR_COUNT - 1)
  }

  // ========================================================================
  // Overlay Pixel Pipeline (system clock domain)
  // ========================================================================

  // Track horizontal pixel position (mirrors scandoubler output_address_reg)
  val hPixel = Reg(UInt(11 bits)) init 0
  // Track vertical VGA line (2× Atari lines due to scandoubler doubling)
  val vLine = Reg(UInt(10 bits)) init 0

  val hsyncPrev = RegNext(io.hsyncIn) init True
  val vsyncPrev = RegNext(io.vsyncIn) init True
  val hsyncRising = io.hsyncIn && !hsyncPrev
  val vsyncRising = io.vsyncIn && !vsyncPrev

  // Horizontal: increment on doubled_enable, wrap at 1823
  when(io.doubledEnable) {
    hPixel := hPixel + 1
    when(hPixel === 1823) {
      hPixel := 0
      vLine := vLine + 1
    }
  }

  // Atari hsync rising edge: new Atari line (also a VGA line boundary)
  when(hsyncRising) {
    hPixel := 0
    vLine := vLine + 1
  }

  // Vsync rising edge: new frame
  when(vsyncRising) {
    vLine := 0
  }

  // VBlank status: based on vsync_in (active low during vsync)
  val vblankSync = !io.vsyncIn

  // Stage 0: Compute character address from pixel position
  val textH_s0 = (hPixel - hTextStart).resize(11)
  val textV_s0 = (vLine - vTextStart).resize(10)
  val charCol_s0 = (textH_s0 >> 3).resize(7)
  val charRow_s0 = (textV_s0 >> 4).resize(5)
  val inTextArea_s0 = (hPixel >= hTextStart) && (hPixel < (hTextStart + COLS * CHAR_W)) &&
                      (vLine >= vTextStart) && (vLine < (vTextStart + ROWS * CHAR_H))
  val charAddr_s0 = ((charRow_s0 << 6) + (charRow_s0 << 4) + charCol_s0).resize(log2Up(CHAR_COUNT))

  // Stage 1: Read character and attribute RAM (1-cycle readSync latency)
  val charData_s1 = charRam.readSync(charAddr_s0)
  val attrData_s1 = attrRam.readSync(charAddr_s0)
  val vRow_s1 = RegNext(textV_s0(3 downto 0))
  val textH_s1 = RegNext(textH_s0)
  val inTextArea_s1 = RegNext(inTextArea_s0) init False

  // Stage 2: Read font ROM (1-cycle readSync latency)
  val fontAddr_s1 = (charData_s1.asUInt << 4).resize(12) + vRow_s1.resize(12)
  val fontByte_s2 = fontRom.readSync(fontAddr_s1)
  val attrData_s2 = RegNext(attrData_s1)
  val textH_s2 = RegNext(textH_s1)
  val inTextArea_s2 = RegNext(inTextArea_s1) init False

  // Stage 3: Select pixel bit and determine color
  val bitIndex_s2 = (U(7, 3 bits) - textH_s2(2 downto 0)).resize(3)
  val fgIndex_s2 = attrData_s2(3 downto 0).asUInt
  val fgColor_s2 = palette(fgIndex_s2)

  val pixelBit_s3 = RegNext(fontByte_s2(bitIndex_s2))
  val fgColor_s3  = RegNext(fgColor_s2)
  val inTextArea_s3 = RegNext(inTextArea_s2) init False

  // Overlay active: foreground text pixel, device enabled, within text area
  val overlayIsActive = pixelBit_s3 && enabled && inTextArea_s3

  // Expand RGB565 → 8-bit per channel (replicate top bits into low bits)
  io.overlayR := Mux(overlayIsActive,
    fgColor_s3(15 downto 11) ## fgColor_s3(15 downto 13),
    B(0, 8 bits))
  io.overlayG := Mux(overlayIsActive,
    fgColor_s3(10 downto 5) ## fgColor_s3(10 downto 9),
    B(0, 8 bits))
  io.overlayB := Mux(overlayIsActive,
    fgColor_s3(4 downto 0) ## fgColor_s3(4 downto 2),
    B(0, 8 bits))
  io.overlayActive := overlayIsActive

  // ========================================================================
  // VSync interrupt (same clock domain — no CDC needed)
  // ========================================================================

  val vsyncEdge = RegNext(vsyncRising) init False
  bus.vsyncInterrupt := vsyncEdge

  // ========================================================================
  // Register Read Mux
  // ========================================================================

  bus.rdData := 0
  switch(bus.addr) {
    is(0) {
      bus.rdData(0) := enabled
      bus.rdData(1) := vblankSync
      bus.rdData(2) := scrollBusy || clearBusy
    }
    is(1) {
      bus.rdData(6 downto 0)  := cursorCol.asBits
      bus.rdData(12 downto 8) := cursorRow.asBits
    }
    is(3) {
      bus.rdData(7 downto 0) := defaultAttr
    }
    is(5) {
      bus.rdData := B(COLS, 32 bits)
    }
    is(6) {
      bus.rdData := B(ROWS, 32 bits)
    }
    is(10) {
      bus.rdData := hTextStart.asBits.resized
    }
    is(11) {
      bus.rdData := vTextStart.asBits.resized
    }
  }

  // ========================================================================
  // Register Write Handling
  // ========================================================================

  when(bus.wr && !scrollBusy && !clearBusy) {
    switch(bus.addr) {
      is(0) {
        enabled := bus.wrData(0)
      }
      is(1) {
        cursorCol := bus.wrData(6 downto 0).asUInt
        cursorRow := bus.wrData(12 downto 8).asUInt
      }
      is(2) {
        val charCode = bus.wrData(7 downto 0)
        val attr     = bus.wrData(15 downto 8)
        val addr     = ((cursorRow << 6) + (cursorRow << 4) + cursorCol).resize(log2Up(CHAR_COUNT))

        sysCharWrEn   := True
        sysCharWrAddr := addr
        sysCharWrData := charCode
        sysAttrWrEn   := True
        sysAttrWrAddr := addr
        sysAttrWrData := attr

        advanceCursor()
      }
      is(3) {
        defaultAttr := bus.wrData(7 downto 0)
      }
      is(4) {
        val index  = bus.wrData(19 downto 16).asUInt
        val rgb565 = bus.wrData(15 downto 0)
        palette(index) := rgb565
      }
      is(7) {
        val charCode = bus.wrData(7 downto 0)
        val attr     = bus.wrData(15 downto 8)
        val col      = bus.wrData(22 downto 16).asUInt
        val row      = bus.wrData(28 downto 24).asUInt
        val addr     = ((row << 6) + (row << 4) + col).resize(log2Up(CHAR_COUNT))

        sysCharWrEn   := True
        sysCharWrAddr := addr
        sysCharWrData := charCode
        sysAttrWrEn   := True
        sysAttrWrAddr := addr
        sysAttrWrData := attr
      }
      is(8) {
        clearBusy   := True
        clearAddr   := 0
        clearEndAddr := (CHAR_COUNT - 1)
      }
      is(9) {
        scrollBusy    := True
        scrollSrcAddr := COLS
        scrollDstAddr := 0
        scrollCount   := ((ROWS - 1) * COLS - 1)
        scrollPhase   := 0
      }
      is(10) {
        hTextStart := bus.wrData(10 downto 0).asUInt
      }
      is(11) {
        vTextStart := bus.wrData(9 downto 0).asUInt
      }
    }
  }

  // ========================================================================
  // HasBusIo implementation
  // ========================================================================

  override def busAddr: UInt   = bus.addr
  override def busRd: Bool     = bus.rd
  override def busWr: Bool     = bus.wr
  override def busWrData: Bits = bus.wrData
  override def busRdData: Bits = bus.rdData
  override def busInterrupts: Seq[Bool] = Seq(bus.vsyncInterrupt)
  override def busExternalIo: Option[Bundle] = Some(io)
}
