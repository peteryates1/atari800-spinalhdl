// VGA text overlay for Atari scandoubler output.
//
// Same JOP bus register interface as jop.io.VgaText (identical register map,
// same character/attribute RAM, font ROM, palette, scroll/clear state machines).
// Java code sees no difference.
//
// Instead of generating its own 25 MHz VGA timing, this device overlays text
// on the scandoubler's pixel stream using doubled_enable / hsync / vsync
// signals from the Atari side.  Everything runs in the system clock domain —
// no separate pixel clock, no CDC.
//
// Background is transparent (overlayActive = False); foreground text pixels
// replace Atari video via the external compositor (VgaOverlayMux).
//
// Register map (same as VgaText):
//   0x0 R: status — bit0=enabled, bit1=vblank, bit2=scrollBusy
//   0x0 W: control — bit0=enable
//   0x1 R/W: cursor position col[6:0], row[12:8]
//   0x2 W: write char+attr at cursor, auto-advance: char[7:0], attr[15:8]
//   0x3 R/W: default attribute [7:0]
//   0x4 W: palette write: index[19:16], rgb565[15:0]
//   0x5 R: columns (80)
//   0x6 R: rows (30)
//   0x7 W: direct write: char[7:0], attr[15:8], col[22:16], row[28:24]
//   0x8 W: clear screen
//   0x9 W: scroll up 1 row
